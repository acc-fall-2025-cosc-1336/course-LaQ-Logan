def display_payroll_check(gross_pay, fica, federal_tax, net_pay): # Display payroll check details
    print(f"Gross Pay: ${gross_pay:.2f}") # Display gross pay formatted to 2 decimal places
    print(f"FICA Tax: ${fica:.2f}") # Display FICA tax formatted to 2 decimal places
    print(f"Federal Tax: ${federal_tax:.2f}") # Display federal tax formatted to 2 decimal places
    print(f"Net Pay: ${net_pay:.2f}") # Display net pay formatted to 2 decimal places